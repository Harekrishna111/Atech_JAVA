public class Assignment_operator {
    public static void main(String [] args){
        int a = 12;
        int b = 4;
        int c = 20;
        int d = 9;
        int e = 1;
        int f = 5;
        a += 2;
        b -= 3;
        c *= 10;
        d /= 1 ;
        e %= 3;
        f = 10;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
